// ── Drill-down tables (§39.6) ──────────────────────────────────────────

fn render_drill_down(rows: &[(DocRow, &DocDiffFile)], ctx: &DocRenderCtx<'_>) -> Option<String> {
    let docrows: Vec<&DocRow> = rows.iter().map(|(r, _)| r).collect();
    let has_en = docrows.iter().any(|r| r.has_english);
    let has_ja = docrows.iter().any(|r| r.has_japanese);

    let structural = render_drill_structural(&docrows, ctx);
    let en_wording = if has_en {
        render_drill_en_wording(&docrows, ctx)
    } else {
        None
    };
    let en_lex = if has_en {
        render_drill_en_lexical(&docrows, ctx)
    } else {
        None
    };
    let ja_comp = if has_ja {
        render_drill_ja(&docrows, ctx)
    } else {
        None
    };

    let filler_block = render_filler_contributors(rows);

    if structural.is_none()
        && en_wording.is_none()
        && en_lex.is_none()
        && ja_comp.is_none()
        && filler_block.is_none()
    {
        return None;
    }

    let mut out = String::new();
    out.push_str(
        "<details>\n<summary>Full metric breakdown (structural \u{00B7} wording \u{00B7} lexical \u{00B7} readability)</summary>\n\n"
    );
    if let Some(t) = structural {
        out.push_str("### Structural / review\n\n");
        out.push_str(&t);
        out.push('\n');
    }
    if let Some(t) = en_wording {
        out.push_str("### English wording quality\n\n");
        out.push_str(&t);
        out.push('\n');
    }
    if let Some(t) = en_lex {
        out.push_str("### English lexical & readability ensemble\n\n");
        out.push_str(&t);
        out.push('\n');
    }
    if let Some(t) = ja_comp {
        out.push_str("### Japanese composition & register\n\n");
        out.push_str(&t);
        out.push('\n');
    }

    if let Some(block) = filler_block {
        out.push_str("### Filler risk contributors (files with risk > 0.40)\n\n");
        out.push_str(&block);
        out.push('\n');
    }

    out.push_str("</details>\n");
    Some(out)
}

fn render_drill_structural(rows: &[&DocRow], ctx: &DocRenderCtx<'_>) -> Option<String> {
    let mut out = String::new();
    out.push_str("| File | RCI | MCC | MRPC | Evidence | Grounding |\n");
    out.push_str("|---|---:|---:|---:|---:|---:|\n");
    for r in rows {
        let _ = writeln!(
            out,
            "| `{path}` | {rci} | {mcc} | {mrpc} | {evi} | {gr} |",
            path = r.display_path,
            rci = r.rci.render(ctx.base_label),
            mcc = r.mcc.render(ctx.base_label),
            mrpc = r.mrpc.render(ctx.base_label),
            evi = r.evidence.render(ctx.base_label),
            gr = r.grounding.render(ctx.base_label),
        );
    }
    Some(out)
}

fn render_drill_en_wording(rows: &[&DocRow], ctx: &DocRenderCtx<'_>) -> Option<String> {
    let mut out = String::new();
    out.push_str("| File | WQS | Passive % | Hedges /100w | Long sent. | Nominalizations |\n");
    out.push_str("|---|---:|---:|---:|---:|---:|\n");
    for r in rows {
        if !r.has_english {
            continue;
        }
        let _ = writeln!(
            out,
            "| `{path}` | {wqs} | {passive} | {hedges} | {ls} | {nom} |",
            path = r.display_path,
            wqs = r.wqs.render(ctx.base_label),
            passive = r.passive_pct.render(ctx.base_label),
            hedges = r.hedges_per_100w.render(ctx.base_label),
            ls = r.long_sentences.render(ctx.base_label),
            nom = r.nominalization_pct.render(ctx.base_label),
        );
    }
    Some(out)
}

fn render_drill_en_lexical(rows: &[&DocRow], ctx: &DocRenderCtx<'_>) -> Option<String> {
    let mut out = String::new();
    out.push_str("| File | MATTR\u{2085}\u{2080} | Hapax | Fog | SMOG | ARI | Coleman-Liau |\n");
    out.push_str("|---|---:|---:|---:|---:|---:|---:|\n");
    for r in rows {
        if !r.has_english {
            continue;
        }
        let smog_cell = if r.sentences_count < 30 {
            String::from("\u{2014}")
        } else {
            r.smog.render(ctx.base_label)
        };
        let _ = writeln!(
            out,
            "| `{path}` | {mattr} | {hapax} | {fog} | {smog} | {ari} | {cl} |",
            path = r.display_path,
            mattr = r.mattr_50.render(ctx.base_label),
            hapax = r.hapax.render(ctx.base_label),
            fog = r.fog.render(ctx.base_label),
            smog = smog_cell,
            ari = r.ari.render(ctx.base_label),
            cl = r.coleman_liau.render(ctx.base_label),
        );
    }
    Some(out)
}

fn render_drill_ja(rows: &[&DocRow], ctx: &DocRenderCtx<'_>) -> Option<String> {
    let mut out = String::new();
    out.push_str(
        "| File | Kanji % | Hiragana % | Katakana % | Avg sent chars | Comma/period | Politeness |\n",
    );
    out.push_str("|---|---:|---:|---:|---:|---:|---:|\n");
    for r in rows {
        if !r.has_japanese {
            continue;
        }
        let politeness = r
            .ja_politeness
            .clone()
            .unwrap_or_else(|| String::from("\u{2014}"));
        let _ = writeln!(
            out,
            "| `{path}` | {k} | {h} | {kt} | {avg} | {cp} | `{pol}` |",
            path = r.display_path,
            k = r.ja_kanji_pct.render(ctx.base_label),
            h = r.ja_hiragana_pct.render(ctx.base_label),
            kt = r.ja_katakana_pct.render(ctx.base_label),
            avg = r.ja_avg_sent_chars.render(ctx.base_label),
            cp = r.ja_comma_period_ratio.render(ctx.base_label),
            pol = politeness,
        );
    }
    Some(out)
}

fn render_filler_contributors(rows: &[(DocRow, &DocDiffFile)]) -> Option<String> {
    let mut any = false;
    let mut out = String::new();
    for (row, file) in rows {
        let Some(head) = file.head.as_ref() else {
            continue;
        };
        if row.filler_value_current <= 0.40 {
            continue;
        }
        any = true;
        let mut contributors = head.ai_era.top_contributors.clone();
        contributors.sort_by(|a, b| {
            b.1.partial_cmp(&a.1)
                .unwrap_or(std::cmp::Ordering::Equal)
                .then(a.0.cmp(&b.0))
        });
        let list: String = contributors
            .iter()
            .take(3)
            .map(|(label, v)| format!("{label} {:.2}", v))
            .collect::<Vec<_>>()
            .join(", ");
        let _ = writeln!(
            out,
            "- **`{path}` ({:.2})** \u{2014} {list}",
            row.filler_value_current,
            path = row.display_path,
        );
    }
    if any { Some(out) } else { None }
}

