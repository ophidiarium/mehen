//! Markdown Documentation Metrics section for the `mehen diff` sticky PR
//! comment, §39 of `docs/mehen_markdown_metrics_research_foundation.md`.
//!
//! Implementation contract — non-negotiable:
//!
//! - Every callout is emitted through the §39.5.2 template catalog. Free-text
//!   prose is forbidden (§39.5.3). The project-level linter
//!   `scripts/check_pr_template_catalog.sh` greps for forbidden phrasings and
//!   for `format!(`/`write!(` outside an allow-list.
//! - Output is a pure function of the `(base, head)` [`MarkdownMetrics`] pair
//!   plus the paths and refs. Byte-identical for identical inputs. Everything
//!   that ends up rendered is sorted.
//! - No network calls. External-link status comes from the Phase-A–E pipeline
//!   which already marks them `resolved: None`.

use std::fmt::Write as _;
use std::path::{Path, PathBuf};

use mehen_markdown::types::{ArtifactKind, LinkClass, MarkdownMetrics};

/// Anchor used so upserts can replace the Markdown-docs block independently
/// of the source-code block.
pub const DOC_ANCHOR: &str = "<!-- mehen-docs -->";

/// Documentation section heading (§39.1).
const DOC_HEADING: &str = "## \u{1F4DD} Documentation Metrics";

/// Footer mirrors the source-code bot (§39.9).
const FOOTER: &str = "> Generated by [mehen](https://github.com/ophidiarium/mehen) \u{2014} the code quality watcher.";

/// Legend printed just above the footer (§39.9).
const LEGEND: &str = "> Legend: \u{1F7E2} improvement \u{00B7} \u{1F534} regression \u{00B7} \u{26A0}\u{FE0F} attention \u{00B7} \u{1F195} new file \u{00B7} \u{26AA} no material change";

/// Max headline-table rows before overflow into `<details>` (§39.8).
const HEADLINE_ROW_CAP: usize = 10;

/// Aggregate-header threshold: above this many files, prepend a count header
/// (§39.8).
const AGGREGATE_FILES_THRESHOLD: usize = 25;

/// Callout cap (§39.5.1, §39.8).
const CALLOUT_CAP: usize = 8;

/// Long-sentence threshold — matches §33.10 plain-prose profile (30 words).
const LONG_SENTENCE_THRESHOLD_DEFAULT: u64 = 30;

/// Readability profile name shown when a grade-target breach is reported.
const READABILITY_DEFAULT_PROFILE: &str = "default";
/// Default FKGL grade target for the plain-prose profile (§31.13).
const READABILITY_DEFAULT_TARGET: f64 = 12.0;

/// Profile max for passive ratio (§33.1, default). Breach → emit template.
const PASSIVE_DEFAULT_MAX: f64 = 0.25;

/// Filler Risk bands per §17.10: `[0.20, 0.40, 0.60, 0.80]`.
const FILLER_BANDS: [f64; 4] = [0.20, 0.40, 0.60, 0.80];
const FILLER_BAND_LABELS: [&str; 5] = ["clean", "mild", "moderate", "high", "severe"];

/// DMI bands per §10.4.
const DMI_BANDS: [(f64, &str); 5] = [
    (0.0, "Poor"),
    (30.0, "Fair"),
    (50.0, "Mixed"),
    (70.0, "Good"),
    (85.0, "Excellent"),
];

/// Evidence-coverage bands per §16.4.
const EVIDENCE_BANDS: [(f64, &str); 4] = [
    (0.0, "absent"),
    (0.25, "weak"),
    (0.50, "partial"),
    (0.75, "grounded"),
];

/// Grounding bands per §15.3.
const GROUNDING_BANDS: [(f64, &str); 4] = [
    (0.0, "absent"),
    (0.25, "weak"),
    (0.50, "partial"),
    (0.75, "grounded"),
];

/// Noticeable-delta thresholds — §39.4 table.
const DMI_NOTICEABLE: f64 = 3.0;
const FKGL_NOTICEABLE: f64 = 0.5;
const TATEISHI_NOTICEABLE: f64 = 2.0;
const LINK_DEBT_NOTICEABLE: f64 = 0.05;
const FILLER_NOTICEABLE: f64 = 0.05;
const FILLER_WARN_THRESHOLD: f64 = 0.60;

/// Per-file input for the Markdown-docs renderer. The analyzer is called
/// once per side and the result attached here.
#[derive(Debug, Clone)]
pub struct DocDiffFile {
    pub path: PathBuf,
    pub head: Option<MarkdownMetrics>,
    pub base: Option<MarkdownMetrics>,
    pub is_new: bool,
    pub is_deleted: bool,
}

impl DocDiffFile {
    pub fn has_data(&self) -> bool {
        self.head.is_some() || self.base.is_some()
    }
}

/// Render context — carries PR-level refs that show up inside cells (file
/// links) and headings (base label).
#[derive(Debug, Clone)]
pub struct DocRenderCtx<'a> {
    pub base_label: &'a str,
    pub repo_url: Option<&'a str>,
    pub head_sha: Option<&'a str>,
    /// Long-sentence threshold in words (§33.10 profile-configurable).
    pub long_sentence_threshold: u64,
    /// Readability profile label used in `readability_target_breach`.
    pub readability_profile: &'a str,
    /// Readability target (grade level) for `readability_target_breach`.
    pub readability_target: f64,
    /// Passive-ratio max for `passive_ratio_breach`.
    pub passive_max: f64,
}

impl<'a> DocRenderCtx<'a> {
    pub fn new(base_label: &'a str) -> Self {
        Self {
            base_label,
            repo_url: None,
            head_sha: None,
            long_sentence_threshold: LONG_SENTENCE_THRESHOLD_DEFAULT,
            readability_profile: READABILITY_DEFAULT_PROFILE,
            readability_target: READABILITY_DEFAULT_TARGET,
            passive_max: PASSIVE_DEFAULT_MAX,
        }
    }
}

// ── Public entry point ─────────────────────────────────────────────────

/// Renders the Markdown-docs section, or returns `None` when no files are
/// eligible (§39.1: suppressed entirely when no `.md` in the PR diff).
pub fn render_doc_section(files: &[DocDiffFile], ctx: &DocRenderCtx<'_>) -> Option<String> {
    let eligible: Vec<&DocDiffFile> = files.iter().filter(|f| f.has_data()).collect();
    if eligible.is_empty() {
        return None;
    }

    // Derive per-file rows and associated callouts. Keep both so ordering is
    // independent: rows sort by severity_class desc/path asc, callouts sort
    // by severity asc/magnitude desc/path asc.
    let mut row_file_pairs: Vec<(DocRow, &DocDiffFile)> = eligible
        .iter()
        .map(|f| (DocRow::build(f, ctx), *f))
        .collect();

    // Build all callouts.
    let mut callouts: Vec<Callout> = Vec::new();
    for (row, file) in &row_file_pairs {
        emit_callouts_for_file(row, file, ctx, &mut callouts);
    }
    callouts.sort_by(|a, b| {
        a.severity
            .cmp(&b.severity)
            .then(
                b.magnitude
                    .partial_cmp(&a.magnitude)
                    .unwrap_or(std::cmp::Ordering::Equal),
            )
            .then(a.path.cmp(&b.path))
            .then(a.rule_id.cmp(b.rule_id))
            .then(a.line_key.cmp(&b.line_key))
    });

    // Sort rows (for headline) by severity desc, path asc.
    row_file_pairs.sort_by(|a, b| {
        b.0.severity_class
            .cmp(&a.0.severity_class)
            .then(a.0.path_key().cmp(&b.0.path_key()))
    });

    let rows: Vec<&DocRow> = row_file_pairs.iter().map(|(r, _)| r).collect();

    let mut out = String::new();
    out.push_str(DOC_ANCHOR);
    out.push('\n');
    out.push_str(DOC_HEADING);
    out.push_str(&heading_scope(ctx.base_label));
    out.push('\n');
    out.push('\n');

    let total_files = rows.len();
    if total_files > AGGREGATE_FILES_THRESHOLD {
        let _ = writeln!(
            out,
            "{total_files} Markdown files changed (top 10 by severity shown)."
        );
        out.push('\n');
    }

    // Headline table — up to HEADLINE_ROW_CAP rows. Overflow into <details>.
    let cap = HEADLINE_ROW_CAP.min(rows.len());
    let (visible, overflow) = rows.split_at(cap);
    write_headline_table(&mut out, visible, ctx);

    if !overflow.is_empty() {
        out.push('\n');
        let _ = writeln!(
            out,
            "<details><summary>{} more file(s)</summary>",
            overflow.len()
        );
        out.push('\n');
        write_headline_table(&mut out, overflow, ctx);
        out.push_str("\n</details>\n");
    }

    let any_short_doc_fkgl = rows.iter().any(|r| r.short_doc_footnote);
    let any_prose_empty = rows.iter().any(|r| r.prose_empty_footnote);
    if any_short_doc_fkgl {
        out.push('\n');
        out.push_str("> \u{00B2} Below grade-scoring threshold (< 100 words or < 5 sentences).\n");
    }
    if any_prose_empty {
        if !any_short_doc_fkgl {
            out.push('\n');
        }
        out.push_str("> \u{00B3} Prose-empty after structural stripping.\n");
    }

    // Callouts — severity-ranked, template-only, 8-cap.
    if !callouts.is_empty() {
        out.push('\n');
        out.push_str("**Callouts**\n");
        out.push('\n');
        let (shown, hidden) = if callouts.len() <= CALLOUT_CAP {
            (callouts.as_slice(), &[] as &[Callout])
        } else {
            callouts.split_at(CALLOUT_CAP)
        };
        for c in shown {
            out.push_str("- ");
            out.push_str(&c.rendered);
            out.push('\n');
        }
        if !hidden.is_empty() {
            out.push('\n');
            let _ = writeln!(
                out,
                "<details><summary>{} more callouts</summary>",
                hidden.len()
            );
            out.push('\n');
            for c in hidden {
                out.push_str("- ");
                out.push_str(&c.rendered);
                out.push('\n');
            }
            out.push_str("\n</details>\n");
        }
    }

    // Drill-down tables (§39.6).
    if let Some(drill) = render_drill_down(&row_file_pairs, ctx) {
        out.push('\n');
        out.push_str(&drill);
    }

    out.push('\n');
    out.push_str(LEGEND);
    out.push('\n');
    out.push('\n');
    out.push_str(FOOTER);
    out.push('\n');

    Some(out)
}

fn heading_scope(base_label: &str) -> String {
    let mut s = String::from(" (this PR vs `");
    s.push_str(base_label);
    s.push_str("`)");
    s
}

// ── DocRow — derived per-file row for the headline table + drill-down ──

#[derive(Debug, Clone)]
struct DocRow {
    path: PathBuf,
    display_path: String,
    link: String,
    mixed_language: bool,
    dominant: Language,
    short_doc_footnote: bool,
    prose_empty_footnote: bool,
    dmi: Cell,
    words: Cell,
    readability: Cell,
    link_debt: Cell,
    filler: Cell,
    filler_value_current: f64,
    rci: Cell,
    mcc: Cell,
    mrpc: Cell,
    evidence: Cell,
    grounding: Cell,
    wqs: Cell,
    passive_pct: Cell,
    hedges_per_100w: Cell,
    long_sentences: Cell,
    nominalization_pct: Cell,
    mattr_50: Cell,
    hapax: Cell,
    fog: Cell,
    smog: Cell,
    ari: Cell,
    coleman_liau: Cell,
    ja_kanji_pct: Cell,
    ja_hiragana_pct: Cell,
    ja_katakana_pct: Cell,
    ja_avg_sent_chars: Cell,
    ja_comma_period_ratio: Cell,
    ja_politeness: Option<String>,
    has_english: bool,
    has_japanese: bool,
    sentences_count: u64,
    severity_class: u8,
}

impl DocRow {
    fn path_key(&self) -> String {
        self.path.to_string_lossy().into_owned()
    }

    fn build(file: &DocDiffFile, ctx: &DocRenderCtx<'_>) -> Self {
        let path = file.path.clone();
        let display_path = path.to_string_lossy().into_owned();
        let link = build_file_link(&display_path, ctx);

        let head = file.head.as_ref();
        let base = file.base.as_ref();

        let (dominant, mixed_language) = resolve_dominant_language(head, base);
        let (has_english, has_japanese) = detect_language_presence(head, base);

        let reference = head.or(base);

        let dmi = Cell::build(
            metric_f64(head, |m| {
                m.maintainability.documentation_maintainability_index
            }),
            metric_f64(base, |m| {
                m.maintainability.documentation_maintainability_index
            }),
            CellKind::Dmi,
            file,
        );
        let words = Cell::build(
            metric_f64(head, |m| m.size.words as f64),
            metric_f64(base, |m| m.size.words as f64),
            CellKind::Words,
            file,
        );

        let mut readability = build_readability_cell(file, dominant);
        let prose_empty_footnote = reference
            .map(|m| m.prose.meta.words_counted == 0 && m.size.words == 0)
            .unwrap_or(false);
        if prose_empty_footnote {
            readability.footnote = Some(Footnote::ProseEmpty);
        }
        let short_doc_footnote = matches!(readability.footnote, Some(Footnote::ShortDoc));

        let link_debt = Cell::build(
            metric_f64(head, |m| m.links.link_debt_score),
            metric_f64(base, |m| m.links.link_debt_score),
            CellKind::LinkDebt,
            file,
        );
        let filler_value_current = metric_f64(head, |m| m.ai_era.filler_lazy_structure_risk);
        let filler = Cell::build(
            filler_value_current,
            metric_f64(base, |m| m.ai_era.filler_lazy_structure_risk),
            CellKind::Filler,
            file,
        );

        let rci = Cell::build(
            metric_f64(head, |m| m.review.review_criticality_index),
            metric_f64(base, |m| m.review.review_criticality_index),
            CellKind::Integer,
            file,
        );
        let mcc = Cell::build(
            metric_f64(head, |m| m.complexity.cognitive_complexity),
            metric_f64(base, |m| m.complexity.cognitive_complexity),
            CellKind::Integer,
            file,
        );
        let mrpc = Cell::build(
            metric_f64(head, |m| m.complexity.reading_path_complexity),
            metric_f64(base, |m| m.complexity.reading_path_complexity),
            CellKind::Integer,
            file,
        );
        let evidence = Cell::build(
            metric_f64(head, |m| m.grounding.evidence_coverage_score),
            metric_f64(base, |m| m.grounding.evidence_coverage_score),
            CellKind::Ratio,
            file,
        );
        let grounding = Cell::build(
            metric_f64(head, |m| m.grounding.repository_grounding_score),
            metric_f64(base, |m| m.grounding.repository_grounding_score),
            CellKind::Ratio,
            file,
        );

        let wqs = english_cell(file, |en| en.wording.wording_quality_score, CellKind::Ratio);
        let passive_pct = english_cell(file, |en| en.wording.passive_ratio, CellKind::Percentage);
        let hedges_per_100w = english_cell(
            file,
            |en| en.wording.hedge_density * 100.0,
            CellKind::OneDecimal,
        );
        let long_sentences = english_cell(
            file,
            |en| en.wording.long_sentence_count as f64,
            CellKind::Integer,
        );
        let nominalization_pct = english_cell(
            file,
            |en| en.wording.nominalization_density,
            CellKind::Percentage,
        );
        let mattr_50 = english_cell(file, |en| en.lexical.mattr_50, CellKind::Ratio);
        let hapax = english_cell(file, |en| en.lexical.hapax_ratio, CellKind::Ratio);
        let fog = english_readability_cell(file, |r| r.gunning_fog);
        let smog = english_readability_cell(file, |r| r.smog);
        let ari = english_readability_cell(file, |r| r.ari);
        let coleman_liau = english_readability_cell(file, |r| r.coleman_liau);

        let ja_kanji_pct = japanese_cell(
            file,
            |ja| ja.script_composition.kanji_ratio,
            CellKind::Percentage,
        );
        let ja_hiragana_pct = japanese_cell(
            file,
            |ja| ja.script_composition.hiragana_ratio,
            CellKind::Percentage,
        );
        let ja_katakana_pct = japanese_cell(
            file,
            |ja| ja.script_composition.katakana_ratio,
            CellKind::Percentage,
        );
        let ja_avg_sent_chars = japanese_cell(
            file,
            |ja| ja.lexical.avg_sentence_chars,
            CellKind::OneDecimal,
        );
        let ja_comma_period_ratio = japanese_cell(
            file,
            |ja| ja.lexical.comma_period_ratio,
            CellKind::OneDecimal,
        );
        let ja_politeness = head
            .and_then(|m| m.prose.japanese.as_ref())
            .map(|ja| ja.wording.politeness_dominant.clone());

        let sentences_count = reference
            .map(|m| m.prose.meta.sentences_counted)
            .unwrap_or(0);

        let mut row = Self {
            path,
            display_path,
            link,
            mixed_language,
            dominant,
            short_doc_footnote,
            prose_empty_footnote,
            dmi,
            words,
            readability,
            link_debt,
            filler,
            filler_value_current,
            rci,
            mcc,
            mrpc,
            evidence,
            grounding,
            wqs,
            passive_pct,
            hedges_per_100w,
            long_sentences,
            nominalization_pct,
            mattr_50,
            hapax,
            fog,
            smog,
            ari,
            coleman_liau,
            ja_kanji_pct,
            ja_hiragana_pct,
            ja_katakana_pct,
            ja_avg_sent_chars,
            ja_comma_period_ratio,
            ja_politeness,
            has_english,
            has_japanese,
            sentences_count,
            severity_class: 0,
        };
        row.severity_class = row.compute_severity();
        row
    }

    fn compute_severity(&self) -> u8 {
        // 4 — objective defects increased or readability regression
        if matches!(self.readability.indicator, Indicator::Regression)
            || matches!(self.link_debt.indicator, Indicator::Regression)
        {
            return 4;
        }
        // 3 — DMI/filler band drops or filler warning.
        if matches!(self.dmi.indicator, Indicator::Regression)
            || self.filler_value_current >= FILLER_WARN_THRESHOLD
            || matches!(self.filler.indicator, Indicator::Regression)
        {
            return 3;
        }
        // 2 — new files (summary shown).
        if matches!(self.dmi.indicator, Indicator::New) {
            return 2;
        }
        // 1 — improvements present.
        if matches!(self.dmi.indicator, Indicator::Improvement)
            || matches!(self.filler.indicator, Indicator::Improvement)
            || matches!(self.readability.indicator, Indicator::Improvement)
            || matches!(self.link_debt.indicator, Indicator::Improvement)
        {
            return 1;
        }
        0
    }
}

fn metric_f64<T>(m: Option<&MarkdownMetrics>, extract: T) -> f64
where
    T: Fn(&MarkdownMetrics) -> f64,
{
    m.map(extract).unwrap_or(0.0)
}

fn english_cell<T>(file: &DocDiffFile, extract: T, kind: CellKind) -> Cell
where
    T: Fn(&mehen_markdown::prose::english::EnglishReport) -> f64,
{
    let head = file
        .head
        .as_ref()
        .and_then(|m| m.prose.english.as_ref())
        .map(&extract)
        .unwrap_or(0.0);
    let base = file
        .base
        .as_ref()
        .and_then(|m| m.prose.english.as_ref())
        .map(&extract)
        .unwrap_or(0.0);
    Cell::build(head, base, kind, file)
}

fn english_readability_cell<T>(file: &DocDiffFile, extract: T) -> Cell
where
    T: Fn(&mehen_markdown::prose::english::readability::ReadabilityReport) -> Option<f64>,
{
    let head = file
        .head
        .as_ref()
        .and_then(|m| m.prose.english.as_ref())
        .and_then(|en| extract(&en.readability))
        .unwrap_or(0.0);
    let base = file
        .base
        .as_ref()
        .and_then(|m| m.prose.english.as_ref())
        .and_then(|en| extract(&en.readability))
        .unwrap_or(0.0);
    Cell::build(head, base, CellKind::OneDecimal, file)
}

fn japanese_cell<T>(file: &DocDiffFile, extract: T, kind: CellKind) -> Cell
where
    T: Fn(&mehen_markdown::prose::japanese::JapaneseReport) -> f64,
{
    let head = file
        .head
        .as_ref()
        .and_then(|m| m.prose.japanese.as_ref())
        .map(&extract)
        .unwrap_or(0.0);
    let base = file
        .base
        .as_ref()
        .and_then(|m| m.prose.japanese.as_ref())
        .map(&extract)
        .unwrap_or(0.0);
    Cell::build(head, base, kind, file)
}

fn build_readability_cell(file: &DocDiffFile, dominant: Language) -> Cell {
    let (head_val, head_short) = readability_value(file.head.as_ref(), dominant);
    let (base_val, base_short) = readability_value(file.base.as_ref(), dominant);
    let kind = match dominant {
        Language::Ja => CellKind::OneDecimalHigherBetter,
        _ => CellKind::OneDecimal,
    };
    let mut cell = Cell::build(head_val.unwrap_or(0.0), base_val.unwrap_or(0.0), kind, file);
    // For a *deleted* file the head side is None by construction. Falling
    // back to the ShortDoc footnote would hide the baseline readability
    // that the doc used to report. Instead, let the Deleted render path
    // preserve the baseline value with the regression indicator (e.g.
    // `0 (was: 12.7) 🔴`) so reviewers still see what was lost.
    if file.is_deleted {
        if base_val.is_none() {
            cell.footnote = Some(Footnote::ShortDoc);
        }
    } else if head_val.is_none() || head_short {
        cell.footnote = Some(Footnote::ShortDoc);
    }
    let _ = base_short;
    cell
}

fn readability_value(m: Option<&MarkdownMetrics>, dominant: Language) -> (Option<f64>, bool) {
    let Some(metrics) = m else {
        return (None, true);
    };
    match dominant {
        Language::Ja => {
            if let Some(ja) = metrics.prose.japanese.as_ref() {
                (ja.readability.tateishi_rs, ja.short_doc_warning)
            } else {
                (None, true)
            }
        }
        _ => {
            if let Some(en) = metrics.prose.english.as_ref() {
                (en.readability.flesch_kincaid_grade, en.short_doc_warning)
            } else {
                (None, true)
            }
        }
    }
}

fn resolve_dominant_language(
    head: Option<&MarkdownMetrics>,
    base: Option<&MarkdownMetrics>,
) -> (Language, bool) {
    let side = head.or(base);
    let Some(m) = side else {
        return (Language::En, false);
    };
    let lang = match m.prose.language_detection.dominant_language.as_str() {
        "ja" => Language::Ja,
        "mixed" => Language::Mixed,
        "en" => Language::En,
        _ => Language::En,
    };
    let mixed = matches!(lang, Language::Mixed);
    let dominant = if mixed {
        let en = m
            .prose
            .language_detection
            .blocks
            .iter()
            .filter(|b| b.language == "en")
            .count();
        let ja = m
            .prose
            .language_detection
            .blocks
            .iter()
            .filter(|b| b.language == "ja")
            .count();
        if ja > en { Language::Ja } else { Language::En }
    } else {
        lang
    };
    (dominant, mixed)
}

fn detect_language_presence(
    head: Option<&MarkdownMetrics>,
    base: Option<&MarkdownMetrics>,
) -> (bool, bool) {
    let has_en = head.is_some_and(|m| m.prose.english.is_some())
        || base.is_some_and(|m| m.prose.english.is_some());
    let has_ja = head.is_some_and(|m| m.prose.japanese.is_some())
        || base.is_some_and(|m| m.prose.japanese.is_some());
    (has_en, has_ja)
}

