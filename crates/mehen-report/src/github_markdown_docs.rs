fn format_link_list(items: &[(u64, String)]) -> String {
    let mut out = String::new();
    for (i, (line, dest)) in items.iter().enumerate() {
        if i > 0 {
            out.push_str(", ");
        }
        let _ = write!(out, "`{}` (L{})", dest, line);
    }
    out
}

fn format_surface_list_without_line(items: &[String], cap: usize) -> String {
    let mut out = String::new();
    for (i, s) in items.iter().take(cap).enumerate() {
        if i > 0 {
            out.push_str(", ");
        }
        let _ = write!(out, "`{s}` (L0)");
    }
    out
}

fn repeat_lines(n: usize) -> String {
    let mut out = String::new();
    for i in 0..n {
        if i > 0 {
            out.push_str(", ");
        }
        out.push_str("L0");
    }
    out
}

